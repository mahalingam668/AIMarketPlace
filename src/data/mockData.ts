import type { AITool, ToolCategory, User, ActivityItem, ChartDataPoint, CategoryStat } from '../types';

interface CategorySeed {
  id: string;
  name: string;
  company: string;
  category: ToolCategory;
  subcategory: string;
  icon: string;
  description: string;
  longDescription: string;
  tags: string[];
  integrations: string[];
  industries: string[];
  regions: string[];
  cloudProviders: string[];
  color: string;
  badge?: AITool['badge'];
  featured?: boolean;
  rating: number;
  reviewCount: number;
  proPrice: number;
  enterprisePrice: number;
  activeUsers: number;
  apiCallsMonthly: number;
  uptime: number;
}

function buildCategoryTool(seed: CategorySeed): AITool {
  return {
    id: seed.id,
    name: seed.name,
    description: seed.description,
    longDescription: seed.longDescription,
    category: seed.category,
    subcategory: seed.subcategory,
    icon: seed.icon,
    rating: seed.rating,
    reviewCount: seed.reviewCount,
    pricing: [
      { name: 'Starter', price: 0, period: 'free', features: [`Core ${seed.tags[0]} tools`, 'Community support', 'Limited monthly usage'] },
      {
        name: 'Professional',
        price: seed.proPrice,
        period: 'monthly',
        features: [`Advanced ${seed.tags[1] ?? seed.tags[0]}`, `${seed.subcategory} workflows`, 'Priority support', 'API access'],
        popular: true,
      },
      { name: 'Enterprise', price: seed.enterprisePrice, period: 'monthly', features: ['Unlimited usage', 'SSO & audit logs', 'Custom SLA', 'Dedicated success manager'] },
    ],
    tags: seed.tags,
    badge: seed.badge,
    integrations: seed.integrations,
    apiCallsMonthly: seed.apiCallsMonthly,
    activeUsers: seed.activeUsers,
    uptime: seed.uptime,
    company: seed.company,
    color: seed.color,
    gradient: `linear-gradient(135deg, ${seed.color}, ${seed.color}99)`,
    featured: seed.featured ?? false,
    industries: seed.industries,
    regions: seed.regions,
    cloudProviders: seed.cloudProviders,
    vendor: seed.company,
  };
}

